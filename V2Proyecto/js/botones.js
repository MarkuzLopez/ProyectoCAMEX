
   function Botonprincipal()
 	 {
 	 	document.getElementById('principal').style.display = 'block';
 	 	document.getElementById('formularios').style.display = 'none';
 	 	document.getElementById('consultas').style.display = 'none';
 	 }

 	 function BotonFormularios()
 	 {
 	 	document.getElementById('formularios').style.display = 'block';
 	 	document.getElementById('principal').style.display = 'none';
 	 	document.getElementById('consultas').style.display = 'none';
 	 }

 	 function BotonConsultas()
 	 {
 	 	document.getElementById('consultas').style.display = 'block';
 	 	document.getElementById('principal').style.display = 'none';
 	 	document.getElementById('formularios').style.display = 'none';
 	 }